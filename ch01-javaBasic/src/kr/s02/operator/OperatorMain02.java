package kr.s02.operator;

public class OperatorMain02 {
	public static void main(String[] args) {
		//산술연산자(+,-,*,/,%)-사칙연산 느낌,계산기 할땐 String str로 이어지더라
		System.out.println(1 + 1);
		System.out.println(10 * 3);
		System.out.println(10 / 3);//몫 구하기
		System.out.println(10 % 3);//나머지 구하기
		//정수값의 기본 데이터 유형인 int로 구분됨
		
		System.out.println(10.0 / 3);
		//실수(double) 정수(int) -> double 값으로 표현
		//자료유형에 따라서 값이 달라짐.
		System.out.println(2.4 * 4);
		//실수(double) 정수(int) -> 2.4*4.0 -> double 값으로 표현
		
		//연산하는 두 수가 다른 자료형이면 맞춰줘야함
		
		
	}
}
